class Solution(object):
    def isSameTree(self, p, q):
        """
        :type p: TreeNode
        :type q: TreeNode
        :rtype: bool
        """
        if p is None and q is None:
          return True
        elif p is None or q is None:
          return False
        else:
          if p.val == q.val:
              return self.isSameTree(p.right,q.right) and self.isSameTree(q.left,p.left)
          return False

